func createInstallClaim(cp claim.Provider) {
	schemaVersion, err := bundle.GetDefaultSchemaVersion()
	if err != nil {
		panic(err)
	}

	b := bundle.Bundle{
		SchemaVersion: schemaVersion,
		Name:          "mybuns",
		Version:       "1.0.0",
		InvocationImages: []bundle.InvocationImage{
			{
				BaseImage: bundle.BaseImage{
					ImageType: driver.ImageTypeDocker,
					Image:     "example.com/myorg/myinstaller",
					Digest:    "sha256:7cc0618539fe11e801ce68911a0c9441a3dfaa9ba63057526c4016cf9db19474",
				},
			},
		},
		Actions: map[string]bundle.Action{
			"logs": {Modifies: false, Stateless: false},
		},
	}

	// Pass an empty set of parameters
	var parameters map[string]interface{}

	// Create the installation record
	const (
		namespace       = ""
		bundleReference = "example.com/myorg/mybuns:v1.0.0"
		bundleDigest    = "sha256:2c26b46b68ffc68ff99b453c1d30413413422d706483bfa0f98a5e886266e7ae"
	)
	i, err := claim.NewInstallation(namespace, "hello", b, bundleReference, bundleDigest)
	if err != nil {
		panic(err)
	}
	err = cp.SaveInstallation(i)
	if err != nil {
		panic(err)
	}

	c, err := claim.New("hello", claim.ActionInstall, b, bundleReference, bundleDigest, parameters)
	if err != nil {
		panic(err)
	}

	err = cp.SaveClaim(c)
	if err != nil {
		panic(err)
	}
}
